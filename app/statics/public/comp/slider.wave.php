          <h5>The Fulani menace and Farming in Northern Ghana; A limitation of the ECOWAS protocol on free movement</h5>
          <p>The original occupation of the people of&nbsp;northern Ghana is farming. However, little increment in food production has been witnessed in the region for the past decades.&nbsp;Amongst the challenges of climate change and poverty, which limits the production of farmers in this region, is the menace caused by Fulani herdsmen, which has contributed to the reduction of farm size by most farmers in the region.&nbsp;</p><p>In other parts of the country, operation 'cow leg ' was instituted to reduce this menace. Agogo and Afram plains were among such places. Many have called for the repatriation of the Fulani herdsmen to their respective countries. However, the provisions of the ECOWAS protocol A/PI/5/79 has endorsed free movement of citizens of member countries between and within the ECOWAS. This throws the situation into a dilemma scale and which has become very dicey. Yes, the free movement facilitates trade among member States, but it also comes with challenges.&nbsp;</p><p>What should we do as a country to avoid or reduce this menace?&nbsp;</p><p>My take;</p><p>&nbsp;A comprehensive data on all Fulani herdsmen should be collated to create a database, so that we can monitor the movement of these herdsmen in the country. This will make it easy to sanction any culprit identified.&nbsp;</p><p>Again, the country should begin looking at allocating a specific geographical location, faraway human settlement, in every region specifically for cattle farmers. This will restrict the increased movement of cattle into farmlands.</p><p>It is worth noting the government has started something good at Afram plains. This should be replicated in major regions where cattle production is high.</p><p>&nbsp;In addition, our chiefs need to be committed to the welfare of their people by ensuring that the areas allocated to Fulani herdsmen are far from farmlands and human settlement.</p><p>Lastly, I think we need a cattle court to facilitate the prosecution of cattle related issues.&nbsp;</p><p>All criticisms are welcome.&nbsp;</p><p>BY&nbsp;</p><p>HARUNA GADO YAKUBU</p>
